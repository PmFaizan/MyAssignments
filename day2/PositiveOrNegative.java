package week1.day2;

public class PositiveOrNegative {

	public static void main(String[] args) {

		int num = 35;
		
		if (num > 0) {
			System.out.println("It is Postive");
		} else if (num < 0) {
			System.out.println("It is Negative");
		}
		else
			System.out.println("It is Neither Positive Nor Negative");
		

	}

}
